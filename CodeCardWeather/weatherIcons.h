int8_t icon2Index (char iconName[] )
{
  if      (strcmp(iconName , "clear-day") == 0)           return 1;
  else if (strcmp(iconName , "clear-night") == 0)         return 2;
  else if (strcmp(iconName , "wind") == 0)                return 3;
  else if (strcmp(iconName , "partly-cloudy-day") == 0)   return 4;
  else if (strcmp(iconName , "cloudy") == 0)              return 4;  // use cloudy-day for cloudy
  else if (strcmp(iconName , "partly-cloudy-night") == 0) return 6;
  else if (strcmp(iconName , "rain") == 0)                return 7;
  else if (strcmp(iconName , "snow") == 0)                return 8;    
  else if (strcmp(iconName , "sleet") == 0)               return 9;        
  else if (strcmp(iconName , "fog") == 0)                 return 10;
  else                                                    return 0;
}
